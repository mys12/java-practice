package pratice;

public class CommentExample {
	public static void main(String[] args) {
	/*
	 * main 함수는 프로그램 실행에 반드시 필요한 함수
	 * main이라고 쓰고 ctrl+space 누르면 자동 생성
	 * 저장은 ctrl+s
	 * 실행은 ctrl+f11
	 * 되돌리기 ctrl+z
	 * 자동 들여쓰기는전체 선택후 ctrl+i
	 */
		
		// sysout이라고 ctrl+space를 누르면 println() 출력문이 자동 생성
		// println() 출력문은 마지막에 \n을 자동으로 넣어줌.
		System.out.println("안녕하세요");
		System.out.println("반가워");
		
		System.out.print("hello\n");
		
		System.out.printf("오늘은 %d년 %d월 %d일 입니다\n",2020, 2, 17);
		
		System.out.println("1"); // 문자열
		System.out.println(1); // 숫자
		
	}
}
