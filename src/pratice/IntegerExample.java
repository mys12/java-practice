package pratice;

public class IntegerExample {
	public static void main(String[] args) {
		
		byte a = 127;
		short b = 32767;
		int c = 2147483647; // 기본형
		long d = 2147483648L; //long 타입은 L을 붙여서 저장
		
		//2진수 형태로 저장, 정수 앞에 0b를 붙임
		int bin = 0b1010;
		System.out.println("2진수 1010 : " + bin);
		
		//8진수 형태로 저장, 정수 앞에 0을 붙임
		int octa = 064;
		System.out.println("8진수 064 : " + octa);
		
		//16진수 형태로 저장, 0x를 붙임
		
		
	}
}
