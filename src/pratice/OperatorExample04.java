package pratice;

public class OperatorExample04 {
	public static void main(String[] args) {
		
		// 대입 연산자
		int a = 5, b = 3;
		
		a += 3; //8 
		b =+ 3; // 3
		
		System.out.println(a);
		System.out.println(b);
		
		a -= 4; // 4
		
		a *= 6; // 24
		
		a /= 5; // 4
		
		a %= 3; // 1
		
		System.out.println(a);
	}
}
